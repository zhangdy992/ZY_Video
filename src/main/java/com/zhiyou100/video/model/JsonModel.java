package com.zhiyou100.video.model;

public class JsonModel {

	private String name;
	private int avergeNum;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAvergeNum() {
		return avergeNum;
	}
	public void setAvergeNum(int avergeNum) {
		this.avergeNum = avergeNum;
	}
	@Override
	public String toString() {
		return "JsonModel [name=" + name + ", avergeNum=" + avergeNum + "]";
	}
	
	
	
	
	
	
	
	
	
	
}
